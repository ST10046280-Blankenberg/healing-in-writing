namespace HealingInWriting.Domain.Stories;

/// <summary>
/// Defines the category of a survivor's story.
/// </summary>
public enum StoryCategory
{
    /// <summary>
    /// Personal survivor story or testimony
    /// </summary>
    SurvivorStory,

    /// <summary>
    /// Healing journey and recovery experience
    /// </summary>
    HealingJourney,

    /// <summary>
    /// Poetry and poetic expression
    /// </summary>
    Poetry,

    /// <summary>
    /// Letter (to self, abuser, or others)
    /// </summary>
    Letter,

    /// <summary>
    /// Reflective or contemplative writing
    /// </summary>
    Reflection,

    /// <summary>
    /// Hope and resilience narrative
    /// </summary>
    HopeAndResilience,

    /// <summary>
    /// Awareness and education piece
    /// </summary>
    Awareness,

    /// <summary>
    /// Other category not listed
    /// </summary>
    Other
}
